print(20 + 10)
print(20 - 10)
print(20 * 10)
print(20 / 10) # Division always return float, this behavior is called as "implicit type casting".
print(20 // 10) # Returns integer. Does division and removes decimal places, doesn't round.
print(20 ** 10) # 20 ^ 10

'''
- Python follows PEMDAS.
- If same precedence, left most operator takes over precedence.
- In case of "**", right most operator takes precedence.
'''